package gaujau;

public class ifElse {

	public static void main(String[] args) {
		int time = 35;
		if (time < 10) {
			System.out.println("Good morning.");
		} else if (time < 20) {
			System.out.println("Good day.");
		} else if (time < 30) {
			System.out.println("Good evening.");
		}
		if (time < 40) {
			System.out.println("Good night.");
		}
	}
}
